CREATE TRIGGER `LOCDEPDLT` 
  BEFORE DELETE ON `LOCDEP` 
  FOR EACH ROW 
    INSERT INTO `LOCDEPA` (`LOCDEPAMET`, `LOCDEPAEMP`, `LOCDEPAUSU`, `LOCDEPAFEC`, `LOCDEPADIP`, `LOCDEPACODOLD`, `LOCDEPAEDCOLD`, `LOCDEPAPACOLD`, `LOCDEPANOMOLD`, `LOCDEPAOBSOLD`)

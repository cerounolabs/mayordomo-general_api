CREATE TRIGGER `LOCDEPINS` 
  AFTER INSERT ON `LOCDEP` 
  FOR EACH ROW 
    INSERT INTO `LOCDEPA` (`LOCDEPAMET`, `LOCDEPAEMP`, `LOCDEPAUSU`, `LOCDEPAFEC`, `LOCDEPADIP`, `LOCDEPACODNEW`, `LOCDEPAEDCNEW`, `LOCDEPAPACNEW`, `LOCDEPANOMNEW`, `LOCDEPAOBSNEW`)
